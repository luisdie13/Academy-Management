/**
 * Get all departments in Guatemala
 * @returns {Array<string>} List of department names
 */
export const getDepartments = () => {
  return Object.keys(GUATEMALA_DEPARTMENTS).sort();
};

/**
 * Get all municipalities for a given department
 * @param {string} department - Department name
 * @returns {Array<string>} List of municipalities (empty if department not found)
 */
export const getMunicipalitiesByDepartment = (department) => {
  return GUATEMALA_DEPARTMENTS[department] || [];
};

/**
 * Check if a department-municipality combination is valid
 * @param {string} department - Department name
 * @param {string} municipality - Municipality name
 * @returns {boolean} True if combination is valid
 */
export const isValidLocation = (department, municipality) => {
  const municipalities = getMunicipalitiesByDepartment(department);
  return municipalities.includes(municipality);
};

/**
 * Get all departments and municipalities as a nested structure
 * Useful for frontend dropdowns
 * @returns {Object} Nested object with departments as keys and municipality arrays as values
 */
export const getLocationHierarchy = () => {
  return GUATEMALA_DEPARTMENTS;
};

export default {
  GUATEMALA_DEPARTMENTS,
  getDepartments,
  getMunicipalitiesByDepartment,
  isValidLocation,
  getLocationHierarchy
};
